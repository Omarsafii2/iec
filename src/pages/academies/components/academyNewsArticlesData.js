/**
 * مقال نشرة الأكاديمية بنفس شكل أخبار الموقع (NewsDetailContent).
 * @param {string} academyId
 * @param {string} newsId
 * @returns {null | { title: string, date: string, category: string, image: string, imageAlt: string, bodyHtml: string }}
 */
export function getAcademyNewsArticle(academyId, newsId) {
  const academy = getAcademyById(academyId);
  if (!academy || !newsId) return null;

  if (academyId === 'arts-culture' && ARTS_CULTURE_NEWS[newsId]) {
    return ARTS_CULTURE_NEWS[newsId];
  }

  const item = academy.newsItems?.find(
    (n) => n.to === `/academies/${academyId}/news/${newsId}` || n.to.endsWith(`/${newsId}`),
  );
  if (!item) return null;

  const imageSrc =
    typeof academy.image === 'string' ? academy.image : (academy.image && academy.image.src) || '/logo.svg';

  return {
    title: item.title,
    date: `${item.day} ${item.monthYear}`,
    category: academy.title,
    image: imageSrc,
    imageAlt: item.title,
    bodyHtml: `
      <p>${item.excerpt}</p>
      <p>للمزيد من التفاصيل يرجى التواصل مع إدارة ${academy.title} أو زيارة صفحة الأكاديمية على موقع النادي.</p>
    `,
  };
}
